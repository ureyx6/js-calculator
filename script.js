function addValues (inputOne, inputTwo) {
 return (inputOne + inputTwo);
}

function subtractValues (inputOne, inputTwo) {
    return (inputOne - inputTwo);
}

function multiplyValues (inputOne, inputTwo) {
    return (inputOne * inputTwo)
}

function divideValues (inputOne, inputTwo) {
    return (inputOne/inputTwo) 
}

